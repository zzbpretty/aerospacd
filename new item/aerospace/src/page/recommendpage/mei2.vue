   <p>Several other members of the astronaut corps also helped perform other tests for tools to be used on the spacewalks. NASA astronaut Chris Cassidy and Canadian Space Agency astronaut Jeremy Hansen assisted with tool development by carrying out trials in both the NBL and the Active Response Gravity Offload System (ARGOS), a facility that simulates reduced gravity environments out of the water.</p>
   <p>In addition to revitalizing an important piece of scientific equipment, the process of creating the tools and procedures for these spacewalks is preparing teams for the types of spacewalks that may be required on Moon and Mars missions. “These are the kind of skills that are going to feed into going to a planetary surface,” Jochim said. “Cutting stainless steel tubing and then connecting new tubes on a thermal system during a spacewalk with the user-friendly mechanisms we have developed, all the while keeping it safe for the crew member, are the types of activities that will help create our processes for tomorrow’s spacewalkers.”

AMS is a joint effort between NASA and the Department of Energy’s Office of Science and is led by Principal Investigator Samuel Ting, a Nobel laureate from the Massachusetts Institute of Technology. The AMS team includes some 600 physicists from 56 institutions in 16 countries from Europe, North America and Asia. The contributions from the various participants were integrated when the AMS was built at the European Organization for Nuclear Research (CERN) outside of Geneva, Switzerland.</p>
