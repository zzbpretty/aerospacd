            <p style="text-align:center;font-size:14px">This picture, photographed during the spacewalk conducted on July 12, 2011, shows the International Space Station with space shuttle Atlantis docked at right. In the center foreground is the Alpha Magnetic Spectrometer experiment installed during the STS-134 mission.</p>
            <p>Many scientists theorize stars, planets and the molecules that comprise them are only less than five percent of the mass-energy content of the universe. The rest is dark matter, invisible matter that cannot be directly detected but can be inferred. The Alpha Magnetic Spectrometer - 02 (AMS-02) has been looking for evidence of this mysterious substance from the vantage point of the International Space Station since 2011. Designed for a three-year mission of sifting through cosmic ray particles, AMS records the number of particles that pass through all its detectors (over 140 billion particles to date), the type of particle and characteristics such as mass, velocity, charge and their direction of travel. The goal is for scientists to track down their sources to help understand dark matter and the origins of the universe.</p>
            <p>AMS has provided hundreds of researchers around the globe with data that can help piece together the puzzle of what the universe is made of and how it began. “None of the AMS results were predicted,” said Nobel Laureate and AMS principal investigator Samuel Ting in a presentation from 2018. He said results so far have provided unique information to physicists and have included the potential detection of rare antimatter that may have traveled from the far reaches of the cosmos. While there have not been definite dark matter findings, AMS has collected a significant amount of data on cosmic rays, how the rays travel through space and what produces them. 
             
As with many items that are exposed to the harsh environment of space, it now needs an upgrade to continue its data collection. Over the course of the next few months, a complex series of spacewalks will go into action. The launch of a Northrop Grumman Cygnus spacecraft (NG-12) scheduled for launch on Nov. 2 will carry the final supplies needed for the spacewalks. Those supplies include the last tools needed to perform the upgrades.</p>
