            <p>At more than eight years on the station, AMS-02 has far outlived its expected three-year lifespan. AMS began showing signs of age in 2014. It has four redundant cooling pumps intended to keep the silicon tracker, one of several detectors on AMS, at a constant temperature while in space. With temperatures that fluctuate by hundreds of degrees while orbiting Earth, a functioning thermal control system is required to support the tracker, and data from the tracker is needed in combination with the data from the other instruments to support the AMS research.  

While only one of these pumps is required to operate at a time, multiple pumps began to fail. In March 2014, one of the cooling pumps stopped working, and another was found to have degraded. In March 2017, researchers switched to the last fully functional pump to keep the science alive. The AMS team realized they would need to take action to keep the scientific instrument going. “A group of us started working on planning the spacewalks to extend the life of AMS and have been now for four years,” said spacewalk task lead Brian Mader.</p>
            <p> AMS was meant to live out its three-year life in space without maintenance and then wind down, having served its purpose. Since AMS was never planned to be serviced, there are no foot restraints nor handrails installed to help astronauts move around the areas to access the cooling system during a spacewalk. It also was not designed with typical spacewalk tools in mind because, at over 300,000 data channels, it was considered too complex to service. “When you put somebody in a big suit with pressurized gloves with limited dexterity, it changes the game entirely. You have to design tools and procedures completely differently,” Mader said.

In addition to the overall complexity of the instrument, astronauts have never before cut and reconnected fluid lines, like those that are part of the thermal control system, during a spacewalk. Scientists and engineers from around the world have been tackling these challenges over the past four years to prepare for the upcoming spacewalks. Now their procedures, tools and training are about to be put to the test.</p>
            <p>The plan is to bypass the old thermal control system, attach a new one off the side of AMS and plug it into the existing system. “It sounds easy, especially if you're on the ground and have lots of different tools that you need, but it's not an area that was set up for spacewalking in any manner,” said AMS spacewalk repair project manager Tara Jochim.

The work to prepare for the spacewalk has involved making, testing and launching more than 20 new tools to the space station. Many are specialized for specific steps of the spacewalk, such as removing the debris shield from AMS or working on the cooling lines. The tools include plumbing instruments to cut into the cooling lines, new screwdriver bits and devices to capture the fasteners the astronauts remove from AMS.</p>
   <p>“The basic concept on removing the fasteners is actually something that they used on the Hubble Space Telescope spacewalks,” said Flight Operations Directorate Spacewalk Lead John Mularski. “You use a tool to grab underneath the fastener before you fully remove it.” The Hubble Space Telescope also required a series of spacewalks to extend the life of the telescope.

These tools have gone through years of iterations and tests here on Earth by scientists, engineers and astronauts. Considerable ingenuity was required to develop the perfect tools for the AMS spacewalks’ very specific needs. Repeated designing, prototyping, experimenting and validating was required to create all of the space ready tools. ESA (European Space Agency) astronaut Luca Parmitano and NASA astronaut Drew Morgan will perform all of the AMS repair spacewalks, and did many practice runs in the Neutral Buoyancy Lab (NBL) pool at NASA’s Johnson Space Center in Houston, Texas.</p>
