CIA’s Mi-17 Helicopter Comes Home
Final Mission of a Valiant Workhorse

Mi17-1.jpg
    Mi-17 helicopter on display at CIA Headquarters

Fifteen days after the attacks of September 11th, 2001—on President George W. Bush’s orders—the Central Intelligence Agency (CIA) deployed a small team into Afghanistan’s Panjshir Valley. Its mission: to launch US operations against al-Qaida and its Taliban supporters. JAWBREAKER, as the operation would be called, was the United States’ first response to those attacks, and stands as an exemplar of the extraordinary capacity of CIA and the broader US government to respond swiftly and decisively in defense of the country. The JAWBREAKER team of seven Agency officers, three aircrew and two Afghan partners boarded a Russian-made, CIA-modified, Mi-17 heavy-duty helicopter on what would become a historic flight.
</p>
<p style="text-align:center"><img src="../../assets/img/mei1.jpeg" alt=""></p>
<p style="text-align:center;font-size:14px">Mi-17 helicopter on display at CIA Headquarters</p>
<p>

Today—exactly 18 years after the members of operation JAWBREAKER set foot in Afghanistan—CIA had the distinct honor of commemorating that mission with the dedication of the Mi-17 that shuttled team JAWBREAKER over the “Hindu Kush and into history.” Adorned with the tail number 9-11-01, the fully-restored Mi-17 helicopter is nestled amongst the trees in a large green space to the northeast of CIA’s Original Headquarters Building. The rocky landscape on which the helicopter sits was designed to mimic the Afghan landscape in which the helicopter served so well. Hundreds gathered at CIA Headquarters in Langley, Virginia, to see the helicopter in its final home and hear from the Agency officers who played a significant role in the success of CIA’s first response.</p>
            <p>Robert Byer, CIA Museum director and curator, opened the ceremony by thanking attendees for joining CIA in celebrating what he described as an “incredibly auspicious day that has been many years in the making.” He briefly recounted the story of how the Mi-17 helicopter came to rest on CIA campus as a “macro-artifact” in CIA Museum’s growing collection. A macro-artifact, Robert explained, simply means that “we couldn’t fit it inside the building.”  </p>
            <p> “In 2006, CIA museum began working on an exhibition about the Agency’s role leading up to Operation Enduring Freedom,” Robert explained. What began as a small collection of photographs and artifacts from those involved in the early response to 9/11 quickly grew to include flight kits, cartography and even a cockpit instrument from the Mi-17. “The aircraft was ubiquitous in the part of the world,” he said. “Rugged and dependable and described by those who flew aboard as ’10,000 parts all trying to come apart at once,’” he explained to laughter from the audience.</p>
            <p>In the fall of 2018, Robert and the rest of the CIA Museum staff reunited that single cockpit instrument with the remaining 9,999 pieces of the Mi-17 with its delivery to CIA campus. “With today’s dedication,” he said, “we now have the full story of CIA’s response to those attacks on American soil. It [the exhibition] serves as a bookend to its 911 counterpart,” Robert said referring to the 9,000 pound rust-colored steel column on the Southwest side of CIA’s Original Headquarters Building that was recovered from World Trade Center 6 in New York City.</p>
            <p style="text-align:center"><img src="../../assets/img/mei2.jpeg" alt=""></p>
            <p style="text-align:center;font-size:14px">Mi-17 helicopter on display at CIA Headquarters</p>
            <p>Robert welcomed Gina Haspel, Director of the Central Intelligence Agency, to the stage to introduce the ceremony’s keynote speaker, Gary Schroen, who delayed his retirement to lead Operation JAWBREAKER in 2001. “Today’s ceremony is a celebration of the daring spirit that defines the Central Intelligence Agency,” Director Haspel said. She explained the importance of teamwork in the pursuit of success. “Gary and his team were at the tip of the spear, and at every step of the way there was an Agency family, here at Headquarters and across the world, who had their back.”</p>
            <p>Director Haspel spoke of the courage and motivation of the JAWBREAKER team in their pursuit to “right a terrible wrong.” Her hope for this helicopter is that it serves as a reminder of the sacrifices made in defense of freedom and that visitor’s “gain a deeper appreciation of what it takes to keep our country safe and free.”</p>
            <p>Gary took the stage to generous applause, a clear indication of the respect and admiration he commanded from those in attendance. He thanked Director Haspel for her remarks before launching into his recollection of the time, the operation and the sequence of events that led to his team landing in Afghanistan just two weeks after the attacks on American soil. “It’s an awkward looking piece of machinery,” he began. “But don’t be fooled – the Russians built it for utility and service, rather than looks and style.” He described the helicopter as a workhorse “designed to take a punishment,” which was exactly what the CIA needed.

He recounted the shudder of the helicopter as it began its ascent over the 14,500-foot Anjuman Pass and into Panjshir for the first time—a recollection that would make even the most valiant palms a bit sweaty. “We were very heavy,” he admitted. Between the passengers, weapons, fuel, ammunition and all of the other equipment, the team was pushing the helicopter’s payload to its outer extremes. “It wasn’t ‘business as usual,’” Gary recalled. “But looking around the compartment, you would think it was – no one was dwelling on the danger we were in.”</p>
            <p style="text-align:center"><img src="../../assets/img/mei3.jpeg" alt=""></p>
            <p style="text-align:center;font-size:14px">The Russian-made Mi-17 helicopter was CIA’s workhorse for transporting personnel, equipment, and supplies into, across, and out of Afghanistan. Two days later, the CIA flight mechanic repainted the Mi-17 with a new tail number – 91101.</p>
            <p>Echoing Director Haspel’s comments on teamwork, Gary noted that the success of the team was not theirs alone, but that of the “heroic efforts that this organization [CIA] made in getting the JAWBREAKER team ready.” He pointed to a number of officers and offices across the Agency that were instrumental to navigating the many processes needed to get JAWBREAKER airborne. He also credited the foundation which had been laid years prior, namely the relationships built with the Northern Alliance in Afghanistan, which would help JAWBREAKER navigate the unfamiliar territory. </p>
