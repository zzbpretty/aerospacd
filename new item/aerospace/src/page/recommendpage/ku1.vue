            </p>
            <p>人民网北京5月29日电（记者杜燕飞）5月22日，中国航天系统科学与工程研究院院长薛惠锋做客人民网，就钱学森智库的成立、军民融合等相关问题与网友进行在线交流。薛惠锋表示，以六大体系、两大平台为载体的钱学森智库，以及钱学森智库的发声器——钱学森论坛，聚焦于前沿技术、颠覆式技术，进而引领产业的变革，为实现业态的涌现、国家软实力的提升提供了可供参考、可供决策使用的素材和依据。</p>
            <p> 薛惠锋表示，中国航天60年，有它成功的值得自豪的地方。一系列的工程实践，在工程科技领域，把我国军工技术、保障国家的发展推到了一次又一次的高峰。但是，也需要很多方面要完善、不断的成熟壮大的过程。面对当今世界开放复杂的巨系统的环境，不断的发展中国的航天事业，能够保证国家的安全与发展，为实现中国梦提出了更高的要求。

记得在30年前，钱学森就呼吁，中国航天，不仅仅要制造武器装备，为强军富国来服务，更要在思想上引领，在使能技术和颠覆式技术，包括前沿技术上不断的推出和创新。</p>
