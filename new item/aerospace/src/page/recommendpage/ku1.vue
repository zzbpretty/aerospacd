他当时向中央建议，要把纯而又纯的学术创新的研究，放在中国科学院和大专院校；把实践创新产业化的地方，建议中央成立工程院，特别是系统工程院。他认为，至少在航天先成立中国航天的系统工程院，把理论和技术的成果进行产业化，形成系统的集成和提升，最后涌现出新的业态和产业。

薛惠锋说，这是钱老的遗愿，也是当年追求的目标。在学术理论和系统工程的孵化基地产业化中间，有一个中间环节，就是成立中国工程院。而中国工程院所依赖的学科基础是技术科学，这样的话，就把理论和实践中间架起了一个桥梁，这是钱老30年前的遗愿，也是目标。</p>
            <p>  在2016年4月24号中国航天首个航天日这一天，中央和各有关部门成立中国航天系统科学与工程研究院。它的军工代号是中国航天第十二研究院，中央编办和上级部门对十二院的定位有三条。第一条，建设钱学森智库；第二条，支撑航天、服务国家；第三条，作为军民融合产业发展总体单位。

薛惠锋指出，钱学森智库立足于钱老的美国20年、航天27年半、晚年三十年研究，所形成的思想库体系，情报数据体系、网络和信息化体系、基于模型的系统工程仿真体系，专家体系，包括决策支持系统体系，这六大体系依托在机器平台和指挥控制平台上，形成了以这六大体系两个平台为载体的钱学森智库载体。这种载体是一种方法，是一种工具，也是一种智库的武器。它会把多人拍脑袋所取得的成绩，在机器、仿真、模拟、推演情况下，使它更加的科学，更加的客观，更加的理性，更加的有效。

钱学森这样的智库，是为所有的智库打造工具的方法，是智库的智库，但钱学森智库是需要一个发声器，发声器就是钱学森论坛。到目前为止，已经举办了15次钱学森论坛。每一次钱学森论坛，都是钱学森智库所形成的丰硕的成果，向全党全军全国全社会各方面进行展示。包括在各地举办的钱学森智库聚焦数字中国、网信强国和网控南海、少数民族地区军民融合等等方面的问题，也有包括聚焦于前沿技术、颠覆式技术、使能技术所创造的引领产业制度的变革，还有钱学森智库聚焦改革开放40年。</p>
            <p>这些命题，都是钱学森智库通过钱学森论坛发声器所完成的成果。这些成果集聚军委、科技、工程界和军队等等方面的成果，形成了内参，已经陆续上报中办、国办、军办和有关部门，形成了国家软实力的可供参考、可供决策使用的决策素材和依据。”薛惠锋表示。

同时，当今世界是一个开放复杂的巨系统，钱学森创立的系统工程思想从战略、战役、战术上整体布局，跨领域、跨部门、跨层级综合集成，全方位推进我国政治、经济、社会、生态、文化，以及军事、外交等改革，实现从生产需求到设计需求、跟跑走向领跑的跨越。

